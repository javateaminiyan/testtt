package com.ashok.etown2.model;

public class Street_Pojo {

    private String mStreetName,mStreetNo;

    public Street_Pojo(String mStreetName, String mStreetNo) {
        this.mStreetName = mStreetName;
        this.mStreetNo = mStreetNo;
    }

    public String getmStreetName() {
        return mStreetName;
    }

    public void setmStreetName(String mStreetName) {
        this.mStreetName = mStreetName;
    }

    public String getmStreetNo() {
        return mStreetNo;
    }

    public void setmStreetNo(String mStreetNo) {
        this.mStreetNo = mStreetNo;
    }
}
